# *** CARACTERES ESPECIALES ***

# '\n': Representa un salto de línea.
# '\t': Representa un tabulador.
# '\\': Representa una barra invertida.
# '\'': Representa una comilla simple.
# '\"': Representa una comilla doble.
# '\b': Representa un retroceso (backspace).
# '\r': Representa un retorno de carro (carriage return).

print("Hola\nMundo") # \n Salto de línea.
print("Python es \tgenial") # \t tabulador.
print('Sandalaar\' CatBeaR') #Si no agregamos el \ antes de la comilla simple rompe el código, ya que la comilla simple cerraría
print("Carlos\" Vera") #Mismo caso que comilla simple, pero con comilla doble
